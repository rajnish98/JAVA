public class DataType {
  
  public static void main(String args[]){

    byte b = 8;
    System.out.println(b);
    char ch = 'R';
    System.out.println(ch);
    boolean var = true;
    System.out.println(var);
    float price = 10.5f;
    System.out.println(price);
    int number = 25;
    System.out.println(number);
    // long
    // double
    short n = 240;
    System.out.println(n);
  }
}
